/**
 * Enum of types used to represent the type of all expressions, ids, and variables in the IR.
 * 
 * INT: integer
 * BOOL: boolean
 * INTARR: integer array
 * BOOLARR: boolean array
 * NONE: none
 * STRING: string
 */
package edu.mit.compilers.ir;

public enum Type {
	INT, BOOL, INTARR, BOOLARR, NONE, STRING
}
